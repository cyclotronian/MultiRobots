extern void Usart0RxIsr(u8 data);
//串口接收1中断程序
void USART1_IRQHandler(void) {
  unsigned char value;
  if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET) { 
    USART_ClearFlag(USART1, USART_FLAG_ORE);
    value = USART_ReceiveData(USART1);
    Usart0RxIsr(value);
  }
  asm("NOP");
}

